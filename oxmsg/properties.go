package oxmsg

import (
	"strconv"
	"strings"

	"github.com/xianhammer/format/cfb"
)

type Property struct {
	Name string
	ID   PropertyID
	Type PropertyType
}

func ParseProperty(id string) (p *Property, known bool, err error) {
	if !strings.HasPrefix(id, PropertyPrefix) {
		err = ErrPropertyParse
		return
	}

	var v uint64
	src := id[len(PropertyPrefix):]
	if v, err = strconv.ParseUint(src, 16, 32); err != nil {
		return
	}

	propType := PropertyType(v & 0xFFFF)
	propID := PropertyID(v >> 16)
	p = GetPropertyByID(propID)
	if p == nil {
		p = &Property{src[:4], propID, propType}
	} else {
		known = true
	}
	return
}

func (p *Property) Range() *Range {
	return FindRange(p.ID)
}

func (p *Property) Guid() cfb.GUID {
	switch p.ID {
	case PsetPublicStrings:
		return PS_PUBLIC_STRINGS
	case PsetCommon:
		return PSETID_Common
	case PsetAddress:
		return PSETID_Address
	case PsetInternetHeaders:
		return PS_INTERNET_HEADERS
	case PsetAppointment:
		return PSETID_Appointment
	case PsetMeeting:
		return PSETID_Meeting
	case PsetLog:
		return PSETID_Log
	case PsetMessaging:
		return PSETID_Messaging
	case PsetNote:
		return PSETID_Note
	case PsetPostRss:
		return PSETID_PostRss
	case PsetTask:
		return PSETID_Task
	case PsetUnifiedMessaging:
		return PSETID_UnifiedMessaging
	case PsetMAPI:
		return PS_MAPI
	case PsetAirSync:
		return PSETID_AirSync
	case PsetSharing:
		return PSETID_Sharing
	case PsetXmlExtractedEntities:
		return PSETID_XmlExtractedEntities
	case PsetAttachment:
		return PSETID_Attachment
	}
	return cfb.CLSID_NULL
}

